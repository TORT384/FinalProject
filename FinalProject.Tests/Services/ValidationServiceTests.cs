using FinalProject.Services;
using FinalProject.Tests.TestData;

namespace FinalProject.Tests.Services;

public class ValidationServiceTests
{
    private readonly ValidationService _sut = new();

    [Fact]
    public void IsMoveValid_ReturnsFalse_WhenRowContainsSameValue()
    {
        var board = SudokuBoardBuilder.FromRows(
        [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]);

        var isValid = _sut.IsMoveValid(board, row: 0, column: 2, value: 3);

        Assert.False(isValid);
    }

    [Fact]
    public void IsMoveValid_ReturnsTrue_WhenMoveRespectsSudokuRules()
    {
        var board = SudokuBoardBuilder.FromRows(
        [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]);

        var isValid = _sut.IsMoveValid(board, row: 0, column: 2, value: 4);

        Assert.True(isValid);
    }

    [Fact]
    public void IsBoardComplete_ReturnsTrue_ForSolvedBoard()
    {
        var board = SudokuBoardBuilder.FromRows(
        [
            [5, 3, 4, 6, 7, 8, 9, 1, 2],
            [6, 7, 2, 1, 9, 5, 3, 4, 8],
            [1, 9, 8, 3, 4, 2, 5, 6, 7],
            [8, 5, 9, 7, 6, 1, 4, 2, 3],
            [4, 2, 6, 8, 5, 3, 7, 9, 1],
            [7, 1, 3, 9, 2, 4, 8, 5, 6],
            [9, 6, 1, 5, 3, 7, 2, 8, 4],
            [2, 8, 7, 4, 1, 9, 6, 3, 5],
            [3, 4, 5, 2, 8, 6, 1, 7, 9]
        ]);

        var isComplete = _sut.IsBoardComplete(board);

        Assert.True(isComplete);
    }
}
